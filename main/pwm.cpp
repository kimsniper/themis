#include "pwm.hpp"

Pwm::Pwm(PwmHal& hal) : pwm_hal(hal) {}

Pwm_Error_t Pwm::init(uint8_t gpio_num, uint32_t freq_hz, uint32_t resolution_hz) {
    return pwm_hal.setup(gpio_num, freq_hz, resolution_hz);
}

Pwm_Error_t Pwm::setPulseWidth(uint32_t pulse_width_us) {
    return pwm_hal.setCompareValue(pulse_width_us);
}
